     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $page=Page::slug($id)->orWhere('ID',$id)->published()->first();
        if(!$page){
            return response()->json([
                'error'=>true,
                'message'=>'Page Not found!'
            ],404);
        }
        return response()->json([
            'success'=>true,
            'data'=>$page
        ]);
    }

